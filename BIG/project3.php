<section>
   <p>Flexbox is a module that is intended to better structure a website by changing sizes of items within containers and preventing overflow, hence the word “flex” in Flexbox.  Flexbox’s layout is considered “directionally agnostic” meaning that unlike block which is vertically based or inline which is horizontally based, again making Flexbox more “flexible” when it comes to designing a webpage.
</p>
<p>When working with Flexbox there are two concepts to keep in mind, there are two axes that Flexbox operates on, they are the “main axis” and the “cross axis”.  The main axis is defined by the “flex-direction” property, the cross axis will run perpendicular to this.  The four possible values of the “flex-direction property are row, row-reverse, column, and column-reverse.  
</p>
<p>Flexbox is good for scaling, vertically and horizontally aligning, and re-ordering elements within a container, Flexbox is not recommended for page layout.  Scaling with Flexbox is especially useful, before Flexbox people would have to resort to JQuery to achieve proper vertical scaling and now it’s possible without it thanks to Flexbox.  
</p>
<p>Flexbox has made many things simpler but its not without its fault, there is a repository of information on know Flexbox issues, titled “Flexbugs”, the Github repo was created by Philip Walter and is community curated, encouraging people to post know bugs or issues encountered while using Flexbox.  
Flexbox is a vital tool and powerful tool to learn when it comes to web development, its use is fully supported and seems to be the industry standard.
</p>
   
   
