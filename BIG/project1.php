<section>
   
    <p> Mobile apps are a type of software designed solely for mobile and tablet devices.  They only are accessible through directly downloading the app onto the user’s device.  There are also hybrid and cross-platform apps that work on both platforms and use HTML, CSS and JavaScript to operate.  The operation of an app is usually smooth and utilizes functions of the user device such as built-in camera or microphone. This adds a level of quality and positive perception of a product, the main drawback though is finding users willing to download the app, leading to smaller usage and more expense spent on targeting the user base.
    </p> 
    <p> Responsive websites are websites built using a grid system that react and change depending on the viewport of the user.  Unlike apps users can access websites as long as they have an internet connection not requiring a download and opening up the possibility of a higher number of users.  The cost of developing a responsive website compared to an app is less time consuming and cheaper, also the ability to quickly update and implement changes. 
    </p>
    <p> Which is best for business depends on a couple of factors, if the business is small and with a low amount of funds, web development would be the more cost effective solution, being able to create an up and running responsive website is relatively cheap and could effectively convey what a small business wishes to present to its audience.</p>
    <p> While a bigger business that could afford the time and effort put into app development would be able to circumvent the smaller accessibility by spending more on marketing the app and reaping the benefits of a more user streamlined product that will have faster speeds, smoother UX, and better security then a traditional website.   

    </p>
   
